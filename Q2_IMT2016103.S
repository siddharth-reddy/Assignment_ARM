AREA     factorial, CODE, READONLY
     EXPORT __main
     ENTRY 
__main  FUNCTION	
		
		MOV R1, #3	; THREE NUM IN R1,R2,R3
		MOV R2, #4
		MOV R3, #5
		
COMP	CMP R1,R2		; COMPARE R1,R2
		BLT LOOP1	;IF R1<R2 GOTO LOOP1
		BGE LOOP2	;IF R1>R2 GOTO LOOP2

LOOP1   CMP R2,R3		;COMPARE R2 AND R3
		BLT OUT3	;IF R2<R3 GOTO OUT3
		BGE OUT2	;IF R2>R3 GOTO OUT2
		
LOOP2   CMP R1,R3		; COMPARE R1 AND R3
		BLT OUT3	;IF R1<R3 GOTO OUT3
		BGE OUT1	;IF R1>R3 GOTO OUT1
		
OUT1	MOV R4,R1		;ANS IS R1
		B stop

OUT2	MOV R4,R2		;ANS IS R2
		B stop

OUT3	MOV R4,R3		;ANS IS R3
		B stop

		
		
stop    B stop ; stop program
     ENDFUNC
     END 